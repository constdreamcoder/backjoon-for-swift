func isPrime(_ number: Int) -> Bool {
    if number < 2 { return false }
    for i in 2..<number {
        if number % i == 0 {
            return false
        }
    }
    return true
}

let m = Int(readLine()!)!
let n = Int(readLine()!)!

var sum = 0
var minimum = 10_000

for number in m...n {
    if isPrime(number) {
        sum += number
        minimum = min(number, minimum)
    }
}

if sum == 0 && minimum == 10_000 {
    print(-1)
} else {
    print(sum)
    print(minimum)
}
